package com.edu;

public class Division {
	public static void main(String[] args) {
		float m , n , div, mod;
		
		m = 80;
		n = 9;
		div = m / n;
		
		mod = m % n;
		
		System.out.println("Division of " + m + " and " + n + " is " + div);
		
		System.out.println("Modulus of " + m + " and " + n + " is " + mod);
	}
}
